#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb == 0)
		return (0);
	while (i * i < nb)
		i++;
	if ((nb % (i * i)) == 0)
		return (i);
	return (i - 1);
}
