#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*st;

	st = s;
	i = 0;
	while (i < n)
	{
		st[i] = c;
		i++;
	}
	return (st);
}
