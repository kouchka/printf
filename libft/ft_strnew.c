#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	i = 0;
	if (!(str = (char *)malloc(sizeof(char) * size + 1)))
		return (NULL);
	while (size > i)
	{
		str[i] = '\0';
		i++;
	}
	str[i] = '\0';
	return (str);
}
